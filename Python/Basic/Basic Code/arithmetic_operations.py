# Write a python program to perform all sorts of arithmetic operations on 2 given numbers.
number1 = int(input("Enter first number: "))
number2 = int(input("Enter second number: "))

addition = number1 + number2
print(addition)
subtraction = number1 - number2
print(subtraction)
product = number1 * number2
print(product)
int_division = number1 // number2
real_division = number1 / number2
print(int_division)
print(real_division)
remainder = number1 % number2
print(remainder)
exponent = number1 ** number2
print(exponent)